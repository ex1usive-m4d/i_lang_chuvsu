class TokenType():
    NUMBER = 'NUMBER'  # %NUMBER%
    WORD = "WORD"  # %WORD%
    TEXT = "TEXT"  # %TEXT$
    WRITEC = "WRITEC"  # Write to console
    IF = "IF"  # IF
    ELSE = "ELSE"  # ELSE
    WHILE = "WHILE"  # WHILE
    FOR = "FOR"  # FOR
    DO = "DO"
    BREAK = "BREAK"
    CONTINUE = "CONTINUE"
    FUNC = "FUNC"  # FUNC
    PLUS = "PLUS"  # +
    MINUS = "MINUS"  # -
    STAR = "STAR"  # *
    SLASH = "SLASH"  # /
    EQUAL = "EQUAL"  # =
    EQEQ = "EQUALEQUAL"  # ==
    EXCL = "NOT"  # !
    EXCLEQ = "NOT_EQUAL"  # !=
    BAR = "BAR"  # |
    BARBAR = "BARBAR"  # ||
    AMP = "AMP"  # and
    AMPAMP = "AMPAMP"  # logic and
    LT = "LT"  # <
    LTEQ = "LTEQ"  # <=
    GT = "GT"  # >
    GTEQ = "GTEQ"  # >=
    LPAREN = "LPAREN"  # (
    RPAREN = "RPAREN"  # )
    LBRACKET = "LBRACET"  # [
    RBRACKET = "RBRACKET"  # ]
    LBRACE = "LBRACE"  # {
    RBRACE = "RBRACE"  # }
    SEMIC = "SEMIC"  # ;
    COMMA = "COMMA"  # ,
    EOF = "EOF"  # END LINE
