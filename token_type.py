class TokenType():
    NUMBER = 'NUMBER'  # %NUMBER%
    WORD = "WORD"  # %WORD%
    TEXT = "TEXT"  # %TEXT$
    WRITEC = "WRITEC"  # Write to console
    PLUS = "PLUS"  # +
    MINUS = "MINUS"  # -
    STAR = "STAR"  # *
    SLASH = "SLASH"  # /
    EQUAL = "EQUAL"  # =
    LPAREN = "LPAREN"  # (
    RPAREN = "RPAREN"  # )
    EOF = "EOF"  # END LINE
