class TokenType():
    NUMBER = 'NUMBER'  # %NUMBER%
    WORD = "WORD"  # %WORD%
    TEXT = "TEXT"  # %TEXT$
    WRITEC = "WRITEC"  # Write to console
    IF = "IF"  # IF
    ELSE = "ELSE"  # ELSE
    PLUS = "PLUS"  # +
    MINUS = "MINUS"  # -
    STAR = "STAR"  # *
    SLASH = "SLASH"  # /
    EQUAL = "EQUAL"  # =
    LT = "LT"  # <
    GT = "GT"  # >
    LPAREN = "LPAREN"  # (
    RPAREN = "RPAREN"  # )

    EOF = "EOF"  # END LINE
